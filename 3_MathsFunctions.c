#include <stdio.h>

int main(int argc, char const *argv[])
{
    // +, -, *, /, %
    int num1, num2, num3, num4;
    num1 = 12;
    num2 = 2;
    printf("%d + %d = %d \n\n", num1, num2, num1 + num2);
    printf("%d - %d = %d \n\n", num1, num2, num1 - num2);
    printf("%d * %d = %d \n\n", num1, num2, num1 * num2);
    printf("%d / %d = %d \n\n", num1, num2, num1 / num2);
    printf("%d Mod %d = %d \n\n", num1, num2, num1 % num2);

    // +=
    // +-
    // *=
    // /=
    // %=
    // ++ 
    return 0;
}
